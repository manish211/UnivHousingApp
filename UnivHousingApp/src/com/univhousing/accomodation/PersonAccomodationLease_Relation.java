package com.univhousing.accomodation;

public class PersonAccomodationLease_Relation {
	
	private int accomodationId;
	private int personId;
	private int leaseNo;
	private String accomodationType;
	private int permitId;

	public int getAccomodationId() {
		return accomodationId;
	}
	public void setAccomodationId(int accomodationId) {
		this.accomodationId = accomodationId;
	}
	public int getPersonId() {
		return personId;
	}
	public void setPersonId(int personId) {
		this.personId = personId;
	}
	public int getLeaseNo() {
		return leaseNo;
	}
	public void setLeaseNo(int leaseNo) {
		this.leaseNo = leaseNo;
	}
	public String getAccomodationType() {
		return accomodationType;
	}
	public void setAccomodationType(String accomodationType) {
		this.accomodationType = accomodationType;
	}
	public int getPermitId() {
		return permitId;
	}
	public void setPermitId(int permitId) {
		this.permitId = permitId;
	}
	
	

}
